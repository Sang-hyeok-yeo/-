package castingTest;

public class CastingTest4 {
	public static void main(String[] args) {
//integer.parseint > 문자열을 정수형으로 변환
//double.parsedouble > 문자열을 실수형으로 변환
// 		"1" + 9의 결과를 10으로 출력한다.
		System.out.println(Integer.parseInt("1")+9);
	
//		"3.9" + 9의 결과를 12.9로 출력한다.
		System.out.println(Double.parseDouble("3.9")+9);
	
	
	}
}
