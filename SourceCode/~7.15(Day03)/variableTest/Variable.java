package variableTest;

public class Variable {

	public static void main(String[] args) {
		
		int age = 10;
		float f = 3.14f;
		double d = 3.14;
		char c = 'A';
		String name = "여상혁";
		
		System.out.println(age);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(name);
	}
}
